def _url_looks_deep(url: str) -> bool:
    """True เมื่อ URL ดูเป็น deep link ที่ชี้เนื้อหาจริง (ไม่ใช่หน้าแรก/หน้ารวม)"""
    if not url:
        return False
    import urllib.parse
    try:
        p = urllib.parse.urlparse(url)
    except Exception:
        return False
    if not p.netloc:
        return False
    path = (p.path or "").rstrip("/")
    segments = [s for s in path.split("/") if s]
    # ไฟล์เอกสาร (pdf/doc) = deep เสมอ
    if _re.search(r"\.(pdf|docx?|pptx?)($|\?)", url, _re.IGNORECASE):
        return True
    # มี query ที่ระบุ id/หัวข้อ = ชี้เนื้อหาเจาะจง
    if p.query and _re.search(r"(id|doc|article|page|topic|q)=", p.query, _re.IGNORECASE):
        return True
    # bare domain หรือมีแค่ segment เดียวที่เป็นคำทั่วไป -> ตื้นเกินไป
    if len(segments) == 0:
        return False
    if len(segments) == 1 and segments[0].lower() in _URL_GENERIC_SEGMENTS:
        return False
    # ต้องมี path ที่ลึกพอ (>=2 segment) หรือ segment เดียวที่ไม่ใช่คำทั่วไปและยาวพอ (มี slug)
    if len(segments) >= 2:
        return True
    seg = segments[0].lower()
    return seg not in _URL_GENERIC_SEGMENTS and (len(seg) >= 8 or "-" in seg)


# ─── External URL reachability check ─────────────────────────────────────────

_url_verify_cache: dict[str, bool] = {}
_url_text_cache: dict[str, "str | None"] = {}


# สถานะที่แปลว่า "หน้ายังมีอยู่" แม้เซิร์ฟเวอร์จะบล็อก bot ของเรา (เช่น CDC ตอบ 403)
# -- เบราว์เซอร์จริงของผู้ใช้ยังเปิดได้ จึงไม่ถือว่าลิงก์ตาย
_URL_ALIVE_BLOCKED = {401, 403, 405, 406, 429}
# สถานะที่แปลว่า "หน้าหายจริง" -> ถือว่าลิงก์ตาย
_URL_DEAD = {404, 410}


def verify_url_reachable(url: str, timeout: float = URL_VERIFY_TIMEOUT) -> bool:
    """
    ตรวจว่า URL ภายนอก "ยังมีอยู่จริง" หรือไม่ (กันอ้างอิงลิงก์ตาย/หน้าหาย)
    ใช้ stdlib เท่านั้น (ไม่เพิ่ม dependency) — HEAD ก่อน ถ้าไม่รองรับค่อย GET
    ผลลัพธ์ถูก cache ระหว่าง process เพื่อลด latency

    เกณฑ์ (สอดคล้องเจตนา "ต้องกดเปิดได้ ไม่ใช่หน้าหาย"):
      - 2xx/3xx                    -> เปิดได้ (True)
      - 401/403/405/406/429        -> หน้ายังมีอยู่แต่บล็อก bot (True — browser จริงเปิดได้)
      - 404/410                    -> หน้าหายจริง (False)
      - DNS/connection error       -> เข้าไม่ถึงเลย (False)
      - 5xx                        -> เซิร์ฟเวอร์มีอยู่แต่ error ชั่วคราว (True — โดเมนยัง live)
    """
    if not url:
        return False
    if url in _url_verify_cache:
        return _url_verify_cache[url]

    import urllib.request
    import urllib.error

    headers = {"User-Agent": "Mozilla/5.0 (PharmaCare-AI reference checker)"}

    def _classify_http(code: int) -> bool:
        if 200 <= code < 400:
            return True
        if code in _URL_DEAD:
            return False
        if code in _URL_ALIVE_BLOCKED:
            return True
        if code >= 500:            # เซิร์ฟเวอร์ยังมีอยู่ (error ชั่วคราว)
            return True
        return False               # 4xx อื่นๆ ที่ไม่ชัด -> ถือว่าไม่ปลอดภัยพอ

    def _try(method: str):
        req = urllib.request.Request(url, method=method, headers=headers)
        try:
            with urllib.request.urlopen(req, timeout=timeout) as resp:
                return _classify_http(resp.status)
        except urllib.error.HTTPError as e:
            return _classify_http(e.code)
        except urllib.error.URLError:
            return False           # DNS/connection/timeout -> เข้าไม่ถึงจริง
        except Exception:
            return None            # เหตุอื่น -> ลอง method ถัดไป

    ok = _try("HEAD")
    if ok is not True:             # HEAD ไม่ยืนยันว่า alive -> ลอง GET ให้แน่ใจ
        got = _try("GET")
        if got is not None:
            ok = got
    ok = bool(ok)
    _url_verify_cache[url] = ok
    return ok


# markdown link นอก [Ref: ...] เช่น "[ADA Guidelines](https://...)" ที่โมเดลบางครั้งเขียนแทน [Ref]
_MD_LINK_RE = _re.compile(r'\[([^\]\n]{1,120})\]\((https?://[^)\s]+)\)')

# stopword ภาษาอังกฤษที่ไม่ใช้เป็น keyword ตอน content-match
_EN_STOPWORDS = {
    "the", "and", "for", "with", "from", "guideline", "guidelines", "standards",
    "care", "section", "chapter", "part", "online", "www", "http", "https", "org",
    "com", "html", "pdf", "of", "in", "on", "to", "a", "an",
}


def _label_keywords(label: str) -> list[str]:
    """ดึง keyword ภาษาอังกฤษที่ 'จำเพาะ' จาก label (ชื่อยา/ชื่อเอกสาร/หัวข้อ) ไว้ตรวจ content-match"""
    toks = _re.findall(r"[A-Za-z][A-Za-z0-9\-]{3,}", label or "")
    out: list[str] = []
    for t in toks:
        tl = t.lower()
        if tl in _EN_STOPWORDS or tl in out:
            continue
        out.append(tl)
    return out


def _fetch_page_text(url: str, timeout: float = URL_VERIFY_TIMEOUT) -> str | None:
    """
    ดึงข้อความจากหน้า HTML (best-effort, stdlib) เพื่อทำ content-match
    - คืน None ถ้าดึงไม่ได้ หรือไม่ใช่ HTML (เช่น PDF -> ตรวจเนื้อหาแบบข้อความไม่ได้)
    - cache ต่อ process
    """
    if url in _url_text_cache:
        return _url_text_cache[url]
    import urllib.request, urllib.error
    headers = {"User-Agent": "Mozilla/5.0 (PharmaCare-AI reference checker)"}
    text: str | None = None
    try:
        req = urllib.request.Request(url, method="GET", headers=headers)
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            ctype = (resp.headers.get("Content-Type") or "").lower()
            if "html" in ctype or "text" in ctype:
                raw = resp.read(400_000)  # อ่านแค่ต้นหน้าพอ กัน latency
                html = raw.decode(resp.headers.get_content_charset() or "utf-8", "ignore")
                # strip tags -> ข้อความล้วน lowercase
                html = _re.sub(r"(?is)<(script|style).*?</\1>", " ", html)
                text = _re.sub(r"(?s)<[^>]+>", " ", html).lower()
            # non-HTML (PDF ฯลฯ): คืน None -> ข้าม content-match (เชื่อ reachability)
    except Exception:
        text = None
    _url_text_cache[url] = text
    return text


def verify_url_content_match(url: str, label: str) -> str:
    """
    ตรวจว่าหน้าที่ URL ชี้ไป "มีเนื้อหาตรงกับสิ่งที่อ้าง" จริงหรือไม่ (best-effort)
    คืน: "match" | "mismatch" | "unknown"
      - unknown: ตรวจไม่ได้ (ดึงไม่ได้ / เป็น PDF / label ไม่มี keyword อังกฤษ) -> ให้ caller เชื่อ reachability
      - mismatch: ดึงหน้าได้ แต่ไม่พบ keyword จำเพาะเลย -> ลิงก์อาจไม่ตรงเนื้อหา -> ควรถอด
    """
    keywords = _label_keywords(label)
    if not keywords:
        return "unknown"
    text = _fetch_page_text(url)
    if not text:
        return "unknown"
    hits = sum(1 for k in keywords if k in text)
    # พบ keyword จำเพาะอย่างน้อย 1 คำ = ถือว่าเนื้อหาเกี่ยวข้องพอ
    return "match" if hits >= 1 else "mismatch"


def _resolve_external(url: str | None, label: str) -> tuple[str | None, str]:
    """
    ตัดสินสถานะของ external URL: คืน (url_or_None, url_status)
      - shallow  : หน้าแรก/หน้ารวม -> ถอด URL
      - unreachable : เปิดไม่ได้/หน้าหาย -> ถอด URL
      - content_mismatch : เปิดได้แต่เนื้อหาไม่ตรง -> ถอด URL (กันอ้างลิงก์ที่ไม่มีข้อมูลจริง)
      - verified : deep + reachable + (เนื้อหา match หรือ ตรวจเนื้อหาไม่ได้)
      - unverified : ปิด VERIFY_EXTERNAL_URLS
    """
    if not url:
        return None, "unknown"
    if not _url_looks_deep(url):
        return None, "shallow"
    if not VERIFY_EXTERNAL_URLS:
        return url, "unverified"
    if not verify_url_reachable(url):
        return None, "unreachable"
    if VERIFY_EXTERNAL_CONTENT and verify_url_content_match(url, label) == "mismatch":
        return None, "content_mismatch"
    return url, "verified"


def _append_external_refs(sources: list[dict], seen: set, answer: str) -> None:
    """
    แยกอ้างอิงภายนอกจากข้อความคำตอบแล้วเติมเข้า sources จาก 2 รูปแบบ:
      1) [Ref: ... (URL)] หรือ [Ref: ...นอกคู่มือ...]  (รูปแบบมาตรฐาน)
      2) markdown link [ข้อความ](URL) ที่โมเดลเขียนแทน [Ref] -> normalize เป็นแหล่งอ้างอิงด้วย
    ทุก URL ผ่าน _resolve_external: deep + reachable + content-match (ถ้าเนื้อหาไม่ตรง/เปิดไม่ได้ = ถอด URL)
    """
    def _add(label: str, url: str | None) -> None:
        dedup_key = url or label
        if not dedup_key or dedup_key in seen:
            return
        seen.add(dedup_key)
        final_url, url_status = _resolve_external(url, label)
        sources.append({
            "type"           : "external",
            "reference_type" : "external",
            "source"         : label,
            "url"            : final_url,
            "url_status"     : url_status,
            "page"           : None,
            "heading"        : "",   # คอมโพเนนต์มีสีบอกว่าเป็นแหล่งภายนอกอยู่แล้ว ไม่ต้องมีคำกำกับซ้ำ
            "similarity"     : 1.0,
        })

    text = answer or ""

    # (1) [Ref: ...] blocks
    for inner in _REF_BLOCK_RE.findall(text):
        url_match = _URL_RE.search(inner)
        is_external = bool(url_match) or any(m in inner for m in _EXT_MARKERS)
        if not is_external:
            continue  # เป็น [Ref: Guideline, หน้า X] — มี guideline source อยู่แล้ว
        url = url_match.group(1) if url_match else None
        _add(_clean_external_label(inner), url)

    # (2) markdown links [text](http...) นอก [Ref: ...] -> normalize เข้าแผงอ้างอิง
    for m in _MD_LINK_RE.finditer(text):
        label_txt, url = m.group(1).strip(), m.group(2).strip()
        # ข้ามถ้าเป็นส่วนหนึ่งของ [Ref: ...] (label ขึ้นต้นด้วย Ref:) หรือ url นี้เพิ่มไปแล้ว
        if label_txt.lower().startswith("ref:") or url in seen:
            continue
        _add(_clean_external_label(label_txt), url)


# ─── Light reply (smalltalk / meta) — no retrieval, no medical context ───────

def _light_reply(instruction: str) -> str:
    """
    ตอบข้อความเชิงสังคม/meta ด้วย LLM call เดียวแบบเบา (ไม่ retrieve, ไม่ใส่ system prompt
    คลินิก, ไม่ส่ง history เคสเดิม) -> กัน context bleeding และเร็วกว่าเส้นทางปกติ
    """
    _init()
    try:
        model = genai.GenerativeModel(model_name=CHAT_MODEL)
        resp = _call_with_retry(
            model.generate_content,
            instruction,
            generation_config={"temperature": 0.3, "max_output_tokens": 420},
        )
        text = (resp.text or "").strip()
        # กันเผลอหลุด [Ref] หรืออิโมจิมาในคำตอบ social
        text = _re.sub(r"\[Ref:[^\]]*\]", "", text).strip()
        return text or "ยินดีครับ หากมีคำถามเพิ่มเติมสอบถามได้เลย"
    except Exception as e:
        print(f"[RAG] light reply failed: {e}")
        return "ยินดีครับ หากมีคำถามเพิ่มเติมสอบถามได้เลย"


def _gated_reply(question: str, history: list[dict] | None) -> dict | None:
    """
    ถ้าข้อความล่าสุดเป็น smalltalk/meta -> คืน dict คำตอบเบา (ข้าม RAG)
    ถ้าเป็น clinical -> คืน None (ให้ไปเส้นทาง RAG ปกติ)
    """
    intent = classify_message_intent(question, history)
    if intent == "smalltalk":
        return {
            "answer": _light_reply(_SMALLTALK_INSTRUCTION.format(msg=question)),
            "sources": [],
            "chunks_used": 0,
            "intent": intent,
        }
    if intent == "meta_types":
        return {
            "answer": _light_reply(_META_TYPES_INSTRUCTION),
            "sources": [],
            "chunks_used": 0,
            "intent": intent,
        }
    return None


# ─── Generate Answer (non-streaming) ─────────────────────────────────────────

def generate_answer(
    question : str,
    history  : list[dict] = None,
    top_k    : int = TOP_K,
) -> dict:
    """
    RAG pipeline หลัก:
    1. Search relevant chunks
    2. Build context
    3. Generate answer with Gemini

    Returns: {answer, sources, chunks_used}
    """
    _init()

    # Intent gate: ทักทาย/ขอบคุณ/ชม/รับทราบ หรือถาม meta -> ตอบเบา ไม่ retrieve (กัน context bleeding)
    gated = _gated_reply(question, history)
    if gated is not None:
        return gated

    chunks       = search_chunks(question, top_k=top_k)
    weak_context = _best_similarity(chunks) < SIMILARITY_THRESHOLD
    context      = build_context(chunks, weak_context=weak_context)

    # Build conversation history
    gemini_history = []
    if history:
        for msg in history[-MAX_HISTORY:]:
            role = "user" if msg["role"] == "user" else "model"
            gemini_history.append({"role": role, "parts": [msg["content"]]})

    user_message = USER_MESSAGE_TEMPLATE.format(
        question = question,
        context  = context,
    )

    try:
        chat     = _chat_model.start_chat(history=gemini_history)
        response = _call_with_retry(chat.send_message, user_message)
        answer   = response.text
    except Exception as e:
        err_str = str(e)
        if "429" in err_str or "Quota" in err_str:
            answer = "[ระบบ] โควต้าการใช้งาน API เต็มชั่วคราว (Rate Limit) กรุณารอสักครู่ (ประมาณ 10-15 วินาที) แล้วลองใหม่อีกครั้ง"
        elif _is_transient_error(e):
            answer = "[ระบบ] ระบบมีการใช้งานสูงชั่วคราว (High Demand) กรุณาลองอีกครั้งในอีกสักครู่ครับ"
        else:
            answer = f"[ระบบ] เกิดข้อผิดพลาดในการสร้างคำตอบ: {err_str}"

    answer = _sanitize_citations(answer)
    sources, seen = _guideline_sources(chunks, weak_context)
    _append_external_refs(sources, seen, answer)

    return {
        "answer"      : answer,
        "sources"     : sources,
        "chunks_used" : len(chunks),
    }


# ─── Generate Answer (streaming) ─────────────────────────────────────────────

async def generate_answer_stream(
    question : str,
    history  : list[dict] = None,
    top_k    : int = TOP_K,
):
    """
    RAG pipeline แบบ Streaming:
    Yields JSON strings (Server-Sent Events payload).
    """
    _init()

    # Intent gate: social/meta -> ตอบเบา ไม่ retrieve (กัน context bleeding + เร็วกว่า)
    gated = _gated_reply(question, history)
    if gated is not None:
        yield json.dumps({"type": "chunk", "content": gated["answer"]}) + "\n"
        yield json.dumps({
            "type"        : "done",
            "sources"     : [],
            "chunks_used" : 0,
            "full_answer" : gated["answer"],
            "usage"       : {"prompt_tokens": 0, "completion_tokens": 0},
        }) + "\n"
        return

    chunks       = search_chunks(question, top_k=top_k)
    weak_context = _best_similarity(chunks) < SIMILARITY_THRESHOLD
    context      = build_context(chunks, weak_context=weak_context)

    # Build conversation history
    gemini_history = []
    if history:
        for msg in history[-MAX_HISTORY:]:
            role = "user" if msg["role"] == "user" else "model"
            gemini_history.append({"role": role, "parts": [msg["content"]]})

    user_message = USER_MESSAGE_TEMPLATE.format(
        question = question,
        context  = context,
    )

    # แหล่งอ้างอิงจาก Guideline (กรองด้วย similarity จริง) — external refs เติมหลังได้คำตอบ
    sources, seen = _guideline_sources(chunks, weak_context)

    try:
        chat     = _chat_model.start_chat(history=gemini_history)
        response = _call_with_retry(chat.send_message, user_message, stream=True)

        full_answer = ""
        pending = ""
        prompt_tokens = 0
        completion_tokens = 0

        for chunk in response:
            if chunk.text:
                full_answer += chunk.text
                # normalize citation \u0e23\u0e30\u0e2b\u0e27\u0e48\u0e32\u0e07\u0e2a\u0e15\u0e23\u0e35\u0e21 (frontend \u0e40\u0e23\u0e19\u0e40\u0e14\u0e2d\u0e23\u0e4c\u0e02\u0e49\u0e2d\u0e04\u0e27\u0e32\u0e21\u0e2a\u0e14\u0e46)
                pending += chunk.text
                emit, pending = _stream_flush(pending, final=False)
                if emit:
                    yield json.dumps({"type": "chunk", "content": emit}) + "\n"

            # Try to extract usage from chunk
            if hasattr(chunk, "usage_metadata") and chunk.usage_metadata:
                prompt_tokens = chunk.usage_metadata.prompt_token_count
                completion_tokens = chunk.usage_metadata.candidates_token_count

        # flush \u0e2a\u0e48\u0e27\u0e19\u0e17\u0e35\u0e48\u0e01\u0e31\u0e19\u0e44\u0e27\u0e49 (\u0e40\u0e0a\u0e48\u0e19 [Ref: ...] \u0e01\u0e49\u0e2d\u0e19\u0e2a\u0e38\u0e14\u0e17\u0e49\u0e32\u0e22)
        emit, pending = _stream_flush(pending, final=True)
        if emit:
            yield json.dumps({"type": "chunk", "content": emit}) + "\n"

        # Extract usage from response if not found in chunks
        if prompt_tokens == 0 and hasattr(response, "usage_metadata") and response.usage_metadata:
            prompt_tokens = response.usage_metadata.prompt_token_count
            completion_tokens = response.usage_metadata.candidates_token_count

        # normalize citations \u0e17\u0e31\u0e49\u0e07\u0e01\u0e49\u0e2d\u0e19 \u0e41\u0e25\u0e49\u0e27\u0e04\u0e48\u0e2d\u0e22\u0e14\u0e36\u0e07\u0e2d\u0e49\u0e32\u0e07\u0e2d\u0e34\u0e07\u0e20\u0e32\u0e22\u0e19\u0e2d\u0e01 (URL) \u0e08\u0e32\u0e01\u0e02\u0e49\u0e2d\u0e04\u0e27\u0e32\u0e21\u0e04\u0e33\u0e15\u0e2d\u0e1a
        full_answer = _sanitize_citations(full_answer)
        _append_external_refs(sources, seen, full_answer)

        yield json.dumps({
            "type"        : "done",
            "sources"     : sources,
            "chunks_used" : len(chunks),
            "full_answer" : full_answer,
            "usage": {
                "prompt_tokens": prompt_tokens,
                "completion_tokens": completion_tokens
            }
        }) + "\n"

    except Exception as e:
        err_str = str(e)
        if "429" in err_str or "Quota" in err_str:
            err_msg = "[ระบบ] โควต้าการใช้งาน API เต็มชั่วคราว (Rate Limit) กรุณารอสักครู่ (ประมาณ 10-15 วินาที) แล้วลองใหม่อีกครั้ง"
        elif _is_transient_error(e):
            err_msg = "[ระบบ] ระบบมีการใช้งานสูงชั่วคราว (High Demand) กรุณาลองอีกครั้งในอีกสักครู่ครับ"
        else:
            err_msg = f"[ระบบ] เกิดข้อผิดพลาดในการสร้างคำตอบ: {err_str}"
        yield json.dumps({"type": "error", "content": err_msg}) + "\n"


# ─── Cosine Similarity ────────────────────────────────────────────────────────

def cosine_similarity(vec_a: list[float], vec_b: list[float]) -> float:
    """คำนวณ cosine similarity ระหว่าง 2 vectors"""
    a    = np.array(vec_a)
    b    = np.array(vec_b)
    dot  = np.dot(a, b)
    norm = np.linalg.norm(a) * np.linalg.norm(b)
    return float(dot / norm) if norm != 0 else 0.0


# ─── Embedding-based Evaluation ──────────────────────────────────────────────

def evaluate_answer(prediction: str, expectation: str) -> float:
    """
    ประเมินความถูกต้องด้วย cosine similarity ของ embeddings
    """
    _init()
    try:
        vec_pred = embed_query(prediction)
        vec_exp  = embed_query(expectation)
        return cosine_similarity(vec_pred, vec_exp)
    except Exception as e:
        print(f"[ERROR] evaluate_answer: {e}")
        return 0.0


