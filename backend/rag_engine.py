def rewrite_query(query: str, mode: str | None = None) -> str:
    """
    ขยายคำถามด้วย synonym ทางการแพทย์ (static) เพื่อการ retrieval ที่ตรงขึ้น
    - ไม่แก้เจตนาเดิม เพียง "ต่อท้าย" ศัพท์อังกฤษที่ตรงกับ trigger ภาษาไทย
    - ใช้เฉพาะตอน embed/retrieval เท่านั้น
    """
    text = query or ""
    m = (mode or QUERY_REWRITE_MODE or "static").strip().lower()
    if m == "off" or not text.strip():
        return text
    low = text.lower()
    extra: list[str] = []
    seen: set[str] = set()
    for trigger, expansion in _STATIC_EXPANSION_MAP:
        if trigger.lower() in low:
            for term in expansion.split():
                t = term.lower()
                if t not in low and t not in seen:
                    seen.add(t)
                    extra.append(term)
    if not extra:
        return text
    return f"{text} {' '.join(extra)}"


