# ─── Transient-error retry (Gemini 503 / overloaded) ─────────────────────────
_TRANSIENT_MARKERS = (
    "503", "500", "high demand", "overloaded", "unavailable",
    "try again", "internal error", "deadline", "temporarily",
)


def _is_transient_error(err: Exception) -> bool:
    """True เมื่อเป็น error ชั่วคราวที่ retry แล้วมีโอกาสหาย (ไม่ใช่ 429 quota)"""
    s = str(err).lower()
    if "429" in s or "quota" in s or "rate limit" in s:
        return False  # quota เต็ม retry ไม่ช่วย -> ให้ caller แจ้งผู้ใช้รอ
    return any(m in s for m in _TRANSIENT_MARKERS)


def _call_with_retry(fn, *args, **kwargs):
    """
    เรียก fn พร้อม retry เฉพาะ transient error (เช่น 503 high demand) แบบ backoff สั้นๆ
    กัน spike ชั่วคราวไม่ให้ทำทั้งคำตอบล้มเหลว โดยไม่ให้ผู้ใช้รอนานเกินไป
    """
    last = None
    for attempt in range(LLM_RETRY_MAX + 1):
        try:
            return fn(*args, **kwargs)
        except Exception as e:  # noqa: BLE001 — จับกว้างเพื่อดู marker ของ error
            last = e
            if attempt >= LLM_RETRY_MAX or not _is_transient_error(e):
                raise
            wait = LLM_RETRY_BACKOFF * (attempt + 1)
            print(f"[RAG] transient LLM error (attempt {attempt + 1}) -> retry in {wait:.1f}s: {e}")
            time.sleep(wait)
    if last:
        raise last


